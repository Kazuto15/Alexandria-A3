    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Admin a = new Admin();
        AdminDao dao = new AdminDao();
        
        
        a.setNome(nameField.getText());
        a.setEmail(emailField.getText());
        a.setSenha(new String(passField.getPassword()));
        
        try {
            dao.create(a);
            JOptionPane.showMessageDialog(null, "Administrador criado com sucesso!");
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel cadastrar o administrador"+ ex.getMessage());
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

